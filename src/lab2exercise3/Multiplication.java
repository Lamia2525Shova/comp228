package lab2exercise3;

public class Multiplication {

    // Overloaded static method to multiply two integers
    public static int multiply(int a, int b) {
        return a * b;
    }

    // Overloaded static method to multiply two doubles
    public static double multiply(double a, double b) {
        return a * b;
    }

    // Overloaded static method to multiply three integers
    public static int multiply(int a, int b, int c) {
        return a * b * c;
    }
}

